def _count_lines(code: str) -> int:
    return sum(1 for line in code.splitlines() if line.strip())

def _make_block(language: Language, code: str, filename: str, description: str) -> CodeBlock:
    return CodeBlock(language=language, code=code, filename=filename,
                     description=description, line_count=_count_lines(code))

def _error(description: str, code: str, message: str) -> CodeError:
    return CodeError(description=description, error_code=code, message=message)

_EXTENSIONS = {
    Language.PYTHON: ".py", Language.JAVASCRIPT: ".js", Language.TYPESCRIPT: ".ts",
    Language.BASH: ".sh", Language.SQL: ".sql", Language.HTML_CSS: ".html",
    Language.RUST: ".rs", Language.GO: ".go",
}
def _ext(lang: Language) -> str:
    return _EXTENSIONS.get(lang, ".txt")

