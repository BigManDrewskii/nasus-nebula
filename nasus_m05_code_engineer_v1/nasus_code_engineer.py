_PYTHON_BUG_PATTERNS = [
    (r"except\s+Exception\s+as\s+e\s*:\s*pass", "Bare except swallows errors -- log or re-raise."),
    (r"except\s*:",                                  "Bare except catches BaseException incl. KeyboardInterrupt."),
    (r"==\s*None",                                   "Use 'is None' not == None."),
    (r"\.keys\(\)",                                "Iterating dict.keys() is redundant -- iterate dict directly."),
    (r"open\([^,)]+\)(?!\s*as)",                   "File opened without context manager -- use 'with open(...) as f'."),
    (r"time\.sleep\(\d+\)",                       "Blocking sleep -- use await asyncio.sleep() in async context."),
    (r"\bprint\(",                                  "print() found -- use logging for production code."),
    (r"#\s*(TODO|FIXME|HACK)",                       "Unresolved TODO/FIXME/HACK marker in code."),
]

def debug(spec: Spec) -> Union[CodeResult, CodeError]:
    """Analyse context_code for common error patterns."""
    if not spec.context_code.strip():
        return _error("No code provided for debugging", "MISSING_CONTEXT",
                      "Populate context_code with the code you want debugged.")
    findings = []
    if spec.language == Language.PYTHON:
        for pattern, message in _PYTHON_BUG_PATTERNS:
            if re.search(pattern, spec.context_code, re.IGNORECASE):
                findings.append(message)
    if not findings:
        findings.append("No common bug patterns detected. Manual review recommended.")
    block = _make_block(spec.language, spec.context_code,
                        "debugged_code" + _ext(spec.language), "Original code with debug annotations")
    return CodeResult(
        spec_summary=f"Debug {spec.language.value}: {spec.description[:60]}",
        blocks=[block],
        explanation=("Scanned for common anti-patterns via heuristic regex. "
                     "See review_notes. Apply fixes incrementally and re-run tests."),
        review_notes=findings, status=CodeStatus.DONE)

