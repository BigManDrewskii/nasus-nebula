def review(spec: Spec) -> Union[CodeResult, CodeError]:
    """Return a structured code review of context_code."""
    if not spec.context_code.strip():
        return _error("No code provided for review", "MISSING_CONTEXT",
                      "Populate context_code with the code you want reviewed.")
    code, lines, notes = spec.context_code, spec.context_code.splitlines(), []
    notes.append(f"Line count: {len(lines)} total, {_count_lines(code)} non-empty.")
    if spec.language == Language.PYTHON:
        defs   = len(re.findall(r"^\s*def ", code, re.MULTILINE))
        docstr = len(re.findall(r'"""', code)) // 2
        if defs > 0 and docstr < defs:
            notes.append(f"Docstring coverage: {docstr}/{defs} -- add missing docstrings.")
        else:
            notes.append(f"Docstring coverage: {docstr} for {defs} defs -- looks good.")
        typed = len(re.findall(r"def .+\(.*:.*\)", code))
        if typed < defs:
            notes.append(f"Type annotations: {typed}/{defs} typed -- add hints.")
        else:
            notes.append("Type annotations present on all visible function signatures.")
        if "try:" not in code:
            notes.append("No try/except blocks -- consider error handling for external calls.")
        star_imp = re.findall(r"from \S+ import \*", code)
        if star_imp:
            notes.append(f"Wildcard imports: {star_imp} -- use explicit imports.")
    notes.append("Review complete. Apply findings in order of severity.")
    block = _make_block(spec.language, spec.context_code,
                        "reviewed_code" + _ext(spec.language), "Code submitted for review (unchanged)")
    return CodeResult(
        spec_summary=f"Review {spec.language.value}: {spec.description[:60]}",
        blocks=[block],
        explanation="Static analysis review without execution. See review_notes for findings.",
        review_notes=notes, status=CodeStatus.DONE)

