# 6. route_envelope
# ---------------------------------------------------------------------------
def route_envelope(envelope: NasusEnvelope) -> NasusEnvelope:
    """Dispatch NasusEnvelope to correct handler based on spec.task."""
    try:
        payload = envelope.payload or {}
        raw = payload.get("spec")
        if isinstance(raw, dict):
            spec = Spec(
                task=CodeTask(raw["task"]),
                language=Language(raw["language"]),
                description=raw.get("description", ""),
                context_code=raw.get("context_code", ""),
                requirements=raw.get("requirements", []),
                style=CodeStyle(raw.get("style", "clean")),
                max_lines=int(raw.get("max_lines", 200)),
                include_tests=bool(raw.get("include_tests", False)),
                include_docs=bool(raw.get("include_docs", True)),
            )
        elif isinstance(raw, Spec):
            spec = raw
        else:
            raise ValueError("envelope.payload must contain 'spec' key (dict or Spec).")
        dispatch = {
            CodeTask.GENERATE: generate,
            CodeTask.DEBUG:    debug,
            CodeTask.REFACTOR: refactor,
            CodeTask.REVIEW:   review,
            CodeTask.EXPLAIN:  review,
            CodeTask.TEST:     lambda s: generate(Spec(
                task=CodeTask.GENERATE, language=s.language, description=s.description,
                context_code=s.context_code, include_tests=True, style=s.style, max_lines=s.max_lines)),
            CodeTask.CONVERT:  generate,
        }
        handler = dispatch.get(spec.task)
        if not handler:
            raise ValueError(f"No handler for task: {spec.task.value}")
        result = handler(spec)
        if isinstance(result, CodeError):
            envelope.status  = NasusStatus.ERROR
            envelope.payload = {"error": result.to_dict()}
        else:
            envelope.status  = NasusStatus.DONE
            envelope.payload = {"result": result.to_dict()}
    except Exception as exc:  # noqa: BLE001
        envelope.status  = NasusStatus.ERROR
        envelope.payload = {"error": CodeError("Routing failed","ROUTE_ERROR",str(exc)).to_dict()}
    return envelope

