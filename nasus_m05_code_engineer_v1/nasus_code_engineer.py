def analyze_spec(spec: Spec) -> dict:
    """Classify task complexity, detect language patterns and risk flags."""
    dl = spec.description.lower()
    if len(spec.description) < 80 and not spec.context_code:
        complexity = "low"
    elif len(spec.requirements) > 5 or spec.max_lines > 150:
        complexity = "high"
    else:
        complexity = "medium"
    patterns = []
    if any(k in dl for k in ("client","api","http","request","endpoint")): patterns.append("HTTP Client")
    if any(k in dl for k in ("class","object","inherit","base")): patterns.append("OOP")
    if any(k in dl for k in ("async","await","concurrent","parallel")): patterns.append("Async/Await")
    if any(k in dl for k in ("retry","backoff","resilient")): patterns.append("Retry/Backoff")
    if any(k in dl for k in ("test","unit","pytest","jest")): patterns.append("Unit Testing")
    if any(k in dl for k in ("factory","builder","singleton")): patterns.append("Creational Pattern")
    async_needed = any(k in dl for k in ("async","await","httpx"))
    risk_flags = []
    if spec.max_lines < 20 and complexity == "high":
        risk_flags.append("max_lines may be too low for task complexity")
    if spec.task in (CodeTask.DEBUG, CodeTask.REFACTOR) and not spec.context_code:
        risk_flags.append("DEBUG/REFACTOR requested but context_code is empty")
    if any(k in dl for k in ("secret","password","token")):
        risk_flags.append("Description references secrets -- use env vars only")
    return {"complexity": complexity, "patterns": patterns,
            "async_needed": async_needed, "has_context": bool(spec.context_code.strip()),
            "risk_flags": risk_flags}

