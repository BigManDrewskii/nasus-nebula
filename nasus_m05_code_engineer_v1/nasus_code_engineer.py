def refactor(spec: Spec) -> Union[CodeResult, CodeError]:
    """Apply clean-code transformations to context_code."""
    if not spec.context_code.strip():
        return _error("No code provided for refactoring", "MISSING_CONTEXT",
                      "Populate context_code with the code you want refactored.")
    code, notes = spec.context_code, []
    if spec.language == Language.PYTHON:
        cleaned = "\n".join(line.rstrip() for line in code.splitlines())
        if cleaned != code:
            notes.append("Removed trailing whitespace on all lines.")
            code = cleaned
        if not code.endswith("\n"):
            code += "\n"
            notes.append("Added missing trailing newline.")
        magic = re.findall(r"(?<!\w)(\d{2,})(?!\w)", code)
        noisy = [n for n in magic if n not in ("10","100","200","404","500")]
        if noisy:
            notes.append(f"Magic numbers detected: {set(noisy)} -- extract to named constants.")
        long_lines = [i+1 for i, ln in enumerate(code.splitlines()) if len(ln) > 120]
        if long_lines:
            notes.append(f"Lines exceeding 120 chars: {long_lines[:5]}{'...' if len(long_lines)>5 else ''}")
    if not notes:
        notes.append("No automated transformations applied. Code appears clean.")
    block = _make_block(spec.language, code, "refactored_code" + _ext(spec.language),
                        "Refactored version of provided code")
    return CodeResult(
        spec_summary=f"Refactor {spec.language.value}: {spec.description[:60]}",
        blocks=[block],
        explanation=("Applied: whitespace normalisation, trailing newline, "
                     "magic number detection, long-line flagging."),
        review_notes=notes, status=CodeStatus.DONE)

