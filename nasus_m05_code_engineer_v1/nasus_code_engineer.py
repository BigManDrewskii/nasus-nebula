def generate(spec: Spec) -> Union[CodeResult, CodeError]:
    """Generate source code matching the given Spec."""
    if spec.task != CodeTask.GENERATE:
        return _error("Wrong task routed to generate()", "TASK_MISMATCH",
                      f"generate() expects GENERATE, got {spec.task.value}.")
    if spec.language == Language.PYTHON:
        code, fname = _PYTHON_API_CLIENT, "async_api_client.py"
        desc = "Async HTTP API client with retry and context-manager support"
        explain = ("Generated AsyncAPIClient using httpx.AsyncClient with exponential "
                   "back-off retries, context manager lifecycle, and typed convenience methods.")
    elif spec.language == Language.TYPESCRIPT:
        code, fname = _TS_UTILS, "utils.ts"
        desc = "TypeScript utility helpers: clamp, deepClone, sleep, withRetry"
        explain = ("Four typed utilities: clamp, deepClone, sleep, withRetry. "
                   "withRetry uses exponential back-off for resilient async ops.")
    elif spec.language == Language.JAVASCRIPT:
        code = (_TS_UTILS
                .replace(": number", "").replace(": string", "").replace(": boolean", "")
                .replace(": void", "").replace(": unknown", "").replace("<T>", "")
                .replace("(obj: T): T", "(obj)")
                .replace("(value: number, min: number, max: number): number", "(value, min, max)")
                .replace("(ms: number): Promise<void>", "(ms)")
                .replace("fn: () => Promise<T>,", "fn,").replace("): Promise<T> {", ") {"))
        fname, desc = "utils.js", "JavaScript utility helpers (types stripped)"
        explain = "Same utilities as TypeScript version with type annotations removed."
    else:
        lv = spec.language.value
        code = f"# Generated stub for {lv}\n# Task: {spec.description}\n# TODO: implement\n"
        fname, desc = f"generated.{lv}", f"Stub for {lv} -- requires further implementation"
        explain = f"M05 placeholder for {lv}. Full codegen requires LLM backend."

    block = _make_block(spec.language, code, fname, desc)
    tests = []
    if spec.include_tests and spec.language == Language.PYTHON:
        tests.append(_make_block(Language.PYTHON, _PYTHON_TESTS,
                                 "test_async_api_client.py", "pytest unit tests for AsyncAPIClient"))
    result = CodeResult(spec_summary=f"Generate {spec.language.value}: {spec.description[:80]}",
                        blocks=[block], explanation=explain, tests=tests, status=CodeStatus.DONE)
    issues = validate_code_output(result)
    if issues:
        result.review_notes = issues
    return result

